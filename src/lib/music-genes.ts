export interface MusicGene {
  id: string;
  name: string;
  definition: string;
  effect: string;
  type: string;
  promptCue: string;
}

export interface MusicGeneCategory {
  id: MusicGeneCategoryId;
  label: string;
  hint: string;
  genes: MusicGene[];
}

function gene(
  id: string,
  name: string,
  definition: string,
  effect: string,
  type: string,
  promptCue: string,
): MusicGene {
  return { id, name, definition, effect, type, promptCue };
}

