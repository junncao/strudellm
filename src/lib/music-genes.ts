export const musicGenes = musicGeneCategories.flatMap((category) =>
  category.genes.map((item) => ({ ...item, category: category.id })),
);

export type MusicGeneWithCategory = (typeof musicGenes)[number];

export function getMusicGenesByIds(ids: string[]): MusicGeneWithCategory[] {
  const selected = new Set(ids);
  return musicGenes.filter((item) => selected.has(item.id));
}
